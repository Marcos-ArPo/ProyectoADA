    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String nombre = txtNombre.getText().trim();
        String apellidos = txtApellidos.getText().trim();
        String matricula = txtMatricula.getText().trim().toUpperCase();
        String tipoCliente = (String) comboCliente.getSelectedItem();
        
        if (nombre.isEmpty() || apellidos.isEmpty() || matricula.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        controlador.registrarCliente(nombre, apellidos, matricula, tipoCliente);
    }//GEN-LAST:event_btnRegistrarActionPerformed

